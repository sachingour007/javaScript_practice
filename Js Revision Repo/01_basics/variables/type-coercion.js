// In JavaScript, coercion, specifically type coercion, refers to the process of converting a value from one data type to another. This conversion can happen automatically (implicitly) or be performed intentionally by the developer (explicitly).

let age = 20;
let name = "sachin";

console.log(age + name);

let agee = "20";
let namee = 1;

console.log(agee + namee);

console.log("5" + 1);
